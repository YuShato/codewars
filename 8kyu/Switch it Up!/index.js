function switchItUp(number) {
    let msg = ''
    switch (number) {
        case 1:
            msg = 'One'
            break
        case 2:
            msg = 'Two'
            break
        case 3:
            msg = 'Three'
            break
        case 4:
            msg = 'Four'
            break
        case 5:
            msg = 'Five'
            break
        case 6:
            msg = 'Six'
            break
        case 7:
            msg = 'Seven'
            break
        case 8:
            msg = 'Eight'
            break
        case 9:
            msg = 'Nine'
            break
        case 0:
            msg = 'Zero'
            break
        default:
            msg = ''

    }
    return msg
}